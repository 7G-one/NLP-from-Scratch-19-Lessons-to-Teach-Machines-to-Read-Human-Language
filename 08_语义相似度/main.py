def lesson_synonym_dict():
    """第一部分：同义词典方法"""

    print_separator("8.1 同义词典方法")

    print("""
    同义词典是最直观的语义相似度方法。

    核心思想：
      如果两个词在同义词词典中属于同一组，它们就是相似的。

    生活类比：
      想象一本同义词词典：
      - "开心" → 词条 A → 包含：开心、高兴、快乐、愉快...
      - "伤心" → 词条 B → 包含：伤心、难过、悲伤、哀伤...

      "开心"和"高兴"在同一组 → 相似度高
      "开心"和"伤心"在不同组 → 相似度低
    """)

    # 演示 1：同义词查询
    print("-" * 40)
    print("示例 1：同义词查询")

    query_words = ["开心", "电脑", "猫", "学习"]
    for word in query_words:
        synonyms = find_synonyms(word)
        print(f"\n  '{word}' 的同义词: {synonyms if synonyms else '（词典中未收录）'}")

    # 演示 2：同义词相似度
    print("\n" + "-" * 40)
    print("示例 2：同义词语义相似度")

    pairs = [
        ("开心", "高兴", "同义词"),
        ("开心", "快乐", "同义词"),
        ("开心", "伤心", "反义词"),
        ("电脑", "计算机", "同义词"),
        ("猫", "狗", "同类词"),
        ("猫", "苹果", "不同类"),
    ]

    print(f"\n  {'词1':<6} {'词2':<6} {'关系':<8} {'相似度':>6}")
    print("  " + "-" * 35)
    for w1, w2, relation in pairs:
        sim = synonym_similarity(w1, w2)
        print(f"  {w1:<6} {w2:<6} {relation:<8} {sim:>6.2f}")

    # 演示 3：同义词典的局限性
    print("\n" + "-" * 40)
    print("示例 3：同义词典的局限性")
    print("""
    同义词典方法的优点：
      - 简单直观
      - 不需要训练数据
      - 准确度高（对于收录的词）

    同义词典方法的缺点：
      - 覆盖范围有限（没收录的词无法处理）
      - 无法处理新词（如网络用语）
      - 无法捕捉词之间的细微差别
      - 维护成本高（需要人工更新）

    → 解决方案：词向量（下一节）
    """)


def lesson_word_vectors():
    """第二部分：词向量相似度"""

    print_separator("8.2 词向量相似度（Word2Vec 思想）")

    print("""
    词向量是现代 NLP 的核心技术之一。

    核心思想：
      把每个词映射到一个高维空间中的点。
      意思相近的词，在空间中的位置也接近。

    生活类比：
      想象一个巨大的地图：
      - "猫" 在坐标 (0.9, 0.2, 0.0, 0.0, 0.8)
      - "狗" 在坐标 (0.9, 0.2, 0.0, 0.0, 0.8)  → 很近！
      - "汽车" 在坐标 (0.0, 0.0, 0.9, 0.0, 0.5) → 很远！

    Word2Vec 的经典发现：
      king - man + woman ≈ queen
      （国王 - 男人 + 女人 ≈ 女王）
    """)

    # 演示 1：词向量相似度
    print("-" * 40)
    print("示例 1：词向量相似度")

    pairs = [
        ("猫", "狗"),
        ("开心", "高兴"),
        ("开心", "伤心"),
        ("电脑", "计算机"),
        ("猫", "电脑"),
        ("苹果", "香蕉"),
        ("学习", "研究"),
    ]

    print(f"\n  {'词1':<6} {'词2':<8} {'相似度':>8}")
    print("  " + "-" * 25)
    for w1, w2 in pairs:
        sim = word_vector_similarity(w1, w2)
        bar = "#" * int(max(0, sim) * 20)
        print(f"  {w1:<6} {w2:<8} {sim:>8.4f}  {bar}")

    # 演示 2：找相似词
    print("\n" + "-" * 40)
    print("示例 2：找最相似的词")

    query_words = ["猫", "开心", "电脑", "苹果"]

    for word in query_words:
        similar = find_similar_words(word, top_k=3)
        print(f"\n  与 '{word}' 最相似的词:")
        for w, sim in similar:
            bar = "#" * int(max(0, sim) * 20)
            print(f"    {sim:.4f}  {bar:>20}  {w}")

    # 演示 3：词向量空间可视化
    print("\n" + "-" * 40)
    print("示例 3：词向量空间可视化")
    demo_word_vectors()


def lesson_sentence_similarity():
    """第三部分：句子语义相似度"""

    print_separator("8.3 句子语义相似度")

    print("""
    句子语义相似度是在词级别的基础上，计算整个句子的相似度。

    核心思想：
      1. 把句子拆成词
      2. 对于每个词，找到另一个句子中最相似的词
      3. 综合所有词的相似度，得到句子的相似度

    生活类比：
      比较两个人的自我介绍：
      - 小明："我是程序员，喜欢写代码"
      - 小红："我是开发者，热爱编程"

      逐词比较：
      - "我" ↔ "我" → 相同
      - "是" ↔ "是" → 相同
      - "程序员" ↔ "开发者" → 很相似！
      - "喜欢" ↔ "热爱" → 很相似！
      - "写代码" ↔ "编程" → 很相似！
      → 综合得分 → 非常高！
    """)

    # 演示 1：句子相似度对比
    print("-" * 40)
    print("示例 1：句子语义相似度")

    sentence_pairs = [
        ("今天天气好", "今天气候不错", "同义表达"),
        ("开心", "高兴", "同义词"),
        ("我喜欢猫", "我讨厌猫", "反义情感"),
        ("猫很可爱", "狗很可爱", "相似结构"),
        ("学习编程", "研究代码", "相近语义"),
        ("今天天气好", "苹果很好吃", "不同话题"),
    ]

    print(f"\n  {'句子1':<12} {'句子2':<12} {'关系':<10} {'简单':>6} {'加权':>6}")
    print("  " + "-" * 55)

    for s1, s2, relation in sentence_pairs:
        sim_simple = sentence_similarity_simple(s1, s2)
        sim_weighted = sentence_similarity_weighted(s1, s2)
        print(f"  {s1:<12} {s2:<12} {relation:<10} {sim_simple:>6.4f} {sim_weighted:>6.4f}")

    # 演示 2：语义相似 vs 文本相似
    print("\n" + "-" * 40)
    print("示例 2：语义相似 vs 文本相似 的区别")

    examples = [
        ("开心", "高兴", "没有相同字符，但意思一样"),
        ("苹果好吃", "Apple delicious", "完全不同语言，但意思相近"),
        ("我喜欢猫", "我讨厌猫", "只有一个字不同，但意思相反"),
    ]

    for s1, s2, explanation in examples:
        sim = sentence_similarity_simple(s1, s2)
        print(f"\n  '{s1}' vs '{s2}'")
        print(f"    语义相似度: {sim:.4f}")
        print(f"    说明: {explanation}")


def lesson_comprehensive():
    """第四部分：综合对比"""

    print_separator("8.4 第七章 vs 第八章：文本相似度 vs 语义相似度")

    print("""
    让我们对比上一章的"文本相似度"和本章的"语义相似度"：

    | 方法         | 关注点       | "开心"vs"高兴" | "开心"vs"伤心" |
    |-------------|-------------|----------------|----------------|
    | 编辑距离     | 字符操作      | 高（只差一字）   | 高（只差一字）   |
    | Jaccard     | 字符集合      | 中（有重叠）     | 中（有重叠）     |
    | 余弦相似度   | 字符频率      | 中              | 中              |
    | 语义相似度   | 意思          | 很高（同义词！）  | 很低（反义词！） |

    语义相似度能区分"表面相似但意思不同"的情况！
    """)

    # 综合演示
    print("-" * 40)
    print("综合演示：所有方法对比")

    test_cases = [
        ("今天天气好", "今天气候不错", "语义相近"),
        ("开心", "高兴", "同义词"),
        ("开心", "伤心", "反义词"),
        ("猫", "狗", "同类"),
        ("学习编程", "研究代码", "近义"),
    ]

    for s1, s2, desc in test_cases:
        result = compare_semantic_methods(s1, s2)
        print(f"\n  '{s1}' vs '{s2}' ({desc})")
        for method, score in result.items():
            print(f"    {method}: {score:.4f}")


