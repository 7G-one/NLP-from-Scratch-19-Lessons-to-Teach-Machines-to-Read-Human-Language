# 同义词典是最直观的语义相似度方法。
#
# 核心思想：
#   如果两个词在同义词词典中属于同一组，它们就是相似的。
#
# 生活类比：
#   想象一本同义词词典：
#   - "开心" → 词条 A → 包含：开心、高兴、快乐、愉快...
#   - "伤心" → 词条 B → 包含：伤心、难过、悲伤、哀伤...
#
#   如果两个词在同一个词条下，它们就是相似的。
#   "开心"和"高兴"在同一组 → 相似度高
#   "开心"和"伤心"在不同组 → 相似度低
#
# 在实际项目中，常用的同义词典有：
#   - 同义词词林（中文）
#   - WordNet（英文）
#
# ==============================================================================


