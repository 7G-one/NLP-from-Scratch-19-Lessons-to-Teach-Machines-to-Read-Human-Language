def synonym_similarity(word1: str, word2: str) -> float:
    """
    基于同义词典计算两个词的语义相似度

    ━━━━━━━ 生活类比 ━━━━━━━
    就像查字典：如果两个词在同一页（同一组同义词），
    那它们的意思就很接近。

    ━━━━━━━ 规则 ━━━━━━━
    1. 如果两个词完全相同 → 相似度 1.0
    2. 如果两个词在同义词典的同一组 → 相似度 0.8
    3. 如果两个词有交集（互为同义词） → 相似度 0.6
    4. 否则 → 相似度 0.0

    参数：
        word1: 第一个词
        word2: 第二个词

    返回：
        语义相似度（0.0 到 1.0）
    """
    # 规则 1：完全相同
    if word1 == word2:
        return 1.0

    # 规则 2：word1 在同义词典中，word2 是它的同义词
    if word1 in SYNONYM_DICT and word2 in SYNONYM_DICT[word1]:
        return 0.8

    # 规则 3：word2 在同义词典中，word1 是它的同义词
    if word2 in SYNONYM_DICT and word1 in SYNONYM_DICT[word2]:
        return 0.8

    # 规则 4：两个词的同义词集合有交集
    syn1 = SYNONYM_DICT.get(word1, set())
    syn2 = SYNONYM_DICT.get(word2, set())
    if syn1 and syn2 and (syn1 & syn2):
        # 有共同的同义词，说明它们属于相近的语义领域
        return 0.6

    # 规则 5：不在同一组
    return 0.0


def find_synonyms(word: str) -> set:
    """
    查找一个词的所有同义词

    参数：
        word: 要查询的词

    返回：
        同义词集合（不包含原词）
    """
    synonyms = SYNONYM_DICT.get(word, set())
    return synonyms - {word}


