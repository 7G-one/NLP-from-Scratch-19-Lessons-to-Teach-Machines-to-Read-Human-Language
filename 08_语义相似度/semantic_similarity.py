def vector_similarity_manual(vec1: list, vec2: list) -> float:
    """
    计算两个向量的余弦相似度（手动实现，不依赖 numpy）

    ━━━━━━━ 公式 ━━━━━━━
                        vec1 . vec2
    cosine_sim = ─────────────────────────
                  ||vec1|| * ||vec2||

    参数：
        vec1: 第一个向量（数字列表）
        vec2: 第二个向量（数字列表）

    返回：
        余弦相似度（-1 到 1 之间）
    """
    if len(vec1) != len(vec2):
        raise ValueError(f"向量维度不一致: {len(vec1)} vs {len(vec2)}")

    # 点积
    dot_product = sum(a * b for a, b in zip(vec1, vec2))

    # 向量长度
    norm1 = math.sqrt(sum(a * a for a in vec1))
    norm2 = math.sqrt(sum(b * b for b in vec2))

    if norm1 == 0 or norm2 == 0:
        return 0.0

    return dot_product / (norm1 * norm2)


def word_vector_similarity(word1: str, word2: str) -> float:
    """
    基于词向量计算两个词的语义相似度

    ━━━━━━━ 生活类比 ━━━━━━━
    就像在地图上量两个地点的距离：
    - 猫和狗在地图上很近（都是宠物）
    - 猫和汽车在地图上很远（完全不同的东西）

    参数：
        word1: 第一个词
        word2: 第二个词

    返回：
        语义相似度（-1 到 1 之间，通常 0 到 1）
    """
    vec1 = WORD_VECTORS.get(word1)
    vec2 = WORD_VECTORS.get(word2)

    # 如果某个词不在词典中，无法计算
    if vec1 is None or vec2 is None:
        return 0.0

    return vector_similarity_manual(vec1, vec2)


def find_similar_words(word: str, top_k: int = 5) -> list:
    """
    找到与给定词最相似的词

    ━━━━━━━ 生活类比 ━━━━━━━
    就像问："和猫最像的动物是什么？"
    答案可能是：狗、宠物、动物...

    参数：
        word: 查询词
        top_k: 返回前 k 个结果

    返回：
        列表，每个元素是 (词, 相似度)
    """
    vec = WORD_VECTORS.get(word)
    if vec is None:
        return []

    # 计算与所有词的相似度
    similarities = []
    for other_word, other_vec in WORD_VECTORS.items():
        if other_word != word:
            sim = vector_similarity_manual(vec, other_vec)
            similarities.append((other_word, sim))

    # 按相似度降序排列
    similarities.sort(key=lambda x: x[1], reverse=True)

    return similarities[:top_k]


# ==============================================================================
# 第三部分：句子语义相似度
# ==============================================================================
#
# 句子语义相似度是在词级别的基础上，计算整个句子的相似度。
#
# 核心思想：
#   1. 把句子拆成词
#   2. 对于每个词，找到另一个句子中最相似的词
#   3. 综合所有词的相似度，得到句子的相似度
#
# 生活类比：
#   比较两个人的自我介绍：
#   - 小明："我是程序员，喜欢写代码"
#   - 小红："我是开发者，热爱编程"
#
#   逐词比较：
#   - "我" ↔ "我" → 相同
#   - "是" ↔ "是" → 相同
#   - "程序员" ↔ "开发者" → 很相似！
#   - "喜欢" ↔ "热爱" → 很相似！
#   - "写代码" ↔ "编程" → 很相似！
#
#   综合得分 → 非常高！
#
# ==============================================================================


def simple_tokenize(text: str) -> list:
    """
    简单的分词函数（按字符分词）

    在实际项目中，应该使用 jieba 等专业分词工具。
    这里为了教学方便，使用简单的字符级分词。

    参数：
        text: 输入文本

    返回：
        分词结果列表
    """
    # 跳过空白和标点
    tokens = []
    for char in text:
        if char.strip() and char not in "，。！？、；：""''（）【】《》 ":
            tokens.append(char)
    return tokens


def jieba_tokenize(text: str) -> list:
    """
    使用 jieba 进行分词

    如果 jieba 未安装，回退到简单分词。
    """
    try:
        import jieba
        return [w for w in jieba.cut(text) if w.strip()]
    except ImportError:
        return simple_tokenize(text)


def max_word_similarity(word: str, word_list: list) -> float:
    """
    找到一个词与一组词中最高的相似度

    ━━━━━━━ 生活类比 ━━━━━━━
    就像在一堆人中找"最像你的人"：
    - 逐个比较，取最高的相似度

    参数：
        word: 查询词
        word_list: 候选词列表

    返回：
        最高相似度
    """
    max_sim = 0.0

    for other_word in word_list:
        # 优先使用同义词典
        sim = synonym_similarity(word, other_word)

        # 如果同义词典没有结果，使用词向量
        if sim == 0.0:
            sim = word_vector_similarity(word, other_word)
            # 将词向量相似度（-1到1）映射到（0到1）
            sim = max(0.0, sim)

        max_sim = max(max_sim, sim)

    return max_sim


def sentence_similarity_simple(text1: str, text2: str) -> float:
    """
    计算两个句子的语义相似度（简单版本）

    ━━━━━━━ 算法 ━━━━━━━
    1. 对两个句子分词
    2. 对于 text1 中的每个词，找 text2 中最相似的词
    3. 取平均值作为句子相似度
    4. 反向再算一次（text2 → text1）
    5. 取两次的平均值

    这种方法叫 "贪婪匹配" 或 "平均最大相似度"。

    参数：
        text1: 第一个句子
        text2: 第二个句子

    返回：
        句子语义相似度（0.0 到 1.0）
    """
    # 分词
    words1 = jieba_tokenize(text1)
    words2 = jieba_tokenize(text2)

    # 如果其中一个句子为空
    if not words1 or not words2:
        return 0.0

    # 正向：对于 text1 中的每个词，找 text2 中最相似的词
    sim1_total = 0.0
    for w1 in words1:
        sim1_total += max_word_similarity(w1, words2)
    sim1_avg = sim1_total / len(words1)

    # 反向：对于 text2 中的每个词，找 text1 中最相似的词
    sim2_total = 0.0
    for w2 in words2:
        sim2_total += max_word_similarity(w2, words1)
    sim2_avg = sim2_total / len(words2)

    # 取两次的平均值
    return (sim1_avg + sim2_avg) / 2


def sentence_similarity_weighted(text1: str, text2: str) -> float:
    """
    计算两个句子的语义相似度（加权版本）

    ━━━━━━━ 改进点 ━━━━━━━
    相比简单版本，这里考虑了：
    1. 句子长度的影响（避免长度差异过大导致偏差）
    2. 关键词的权重（实词比虚词更重要）

    参数：
        text1: 第一个句子
        text2: 第二个句子

    返回：
        句子语义相似度（0.0 到 1.0）
    """
    words1 = jieba_tokenize(text1)
    words2 = jieba_tokenize(text2)

    if not words1 or not words2:
        return 0.0

    # 计算正向相似度（text1 → text2）
    sim1_scores = []
    for w1 in words1:
        score = max_word_similarity(w1, words2)
        sim1_scores.append(score)

    # 计算反向相似度（text2 → text1）
    sim2_scores = []
    for w2 in words2:
        score = max_word_similarity(w2, words1)
        sim2_scores.append(score)

    # 取两个方向的平均值
    avg1 = sum(sim1_scores) / len(sim1_scores) if sim1_scores else 0
    avg2 = sum(sim2_scores) / len(sim2_scores) if sim2_scores else 0

    # 使用调和平均（类似 F1 值），对不平衡的情况更敏感
    if avg1 + avg2 == 0:
        return 0.0
    return 2 * avg1 * avg2 / (avg1 + avg2)


