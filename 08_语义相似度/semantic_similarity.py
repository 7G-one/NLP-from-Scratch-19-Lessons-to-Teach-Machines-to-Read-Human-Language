def compare_semantic_methods(text1: str, text2: str) -> dict:
    """
    用所有语义相似度方法比较两个文本

    参数：
        text1: 第一段文本
        text2: 第二段文本

    返回：
        包含各种相似度指标的字典
    """
    return {
        "句子相似度（简单）": round(sentence_similarity_simple(text1, text2), 4),
        "句子相似度（加权）": round(sentence_similarity_weighted(text1, text2), 4),
    }


