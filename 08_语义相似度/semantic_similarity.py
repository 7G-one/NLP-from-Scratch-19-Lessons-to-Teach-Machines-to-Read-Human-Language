#
# 词向量（Word Embedding）是现代 NLP 的核心技术之一。
#
# 核心思想：
#   把每个词映射到一个高维空间中的点。
#   意思相近的词，在空间中的位置也接近。
#
# 生活类比：
#   想象一个巨大的地图：
#   - "猫" 在坐标 (100, 200)
#   - "狗" 在坐标 (110, 210)  → 很近！都是宠物
#   - "汽车" 在坐标 (500, 800) → 很远！不是动物
#
#   词向量就是每个词的"坐标"。
#   距离近 = 意思相似
#
# Word2Vec 的经典发现：
#   king - man + woman ≈ queen
#   （国王 - 男人 + 女人 ≈ 女王）
#
# 这说明词向量不仅能捕捉相似性，还能捕捉类比关系！
#
# ==============================================================================


