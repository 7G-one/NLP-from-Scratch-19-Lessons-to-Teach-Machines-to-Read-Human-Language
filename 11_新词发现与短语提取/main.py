def lesson_mutual_information():
    """第一部分：互信息"""

    print_separator("11.1 互信息（Mutual Information）")

    print("""
    互信息衡量一个字符串的"内部凝聚度"。

    核心思想：
      一个词的各个字符之间应该有很强的关联。
      如果字符之间没有关联，那它们只是碰巧拼在一起。

    生活类比：
      想象两个人经常一起出现：
      - "张三" 和 "李四" 经常一起出现 → 他们可能是朋友
      - "张三" 和 "今天" 经常一起出现 → 可能只是巧合

      互信息衡量的就是这种"一起出现的程度"是否超过"随机巧合"。

    互信息高 → 内部凝聚度高 → 更可能是一个词
    互信息低 → 内部凝聚度低 → 可能只是巧合拼在一起
    """)

    # 测试文本
    text = """
    我今天去了北京大学，北京大学是中国最好的大学之一。
    北京大学的校园很美，北京大学的图书馆很大。
    我在北京大学学习自然语言处理。
    自然语言处理是人工智能的一个分支。
    人工智能技术发展很快，深度学习是核心技术。
    """

    # 演示 1：互信息计算
    print("-" * 40)
    print("示例 1：互信息计算")

    words = ["北京", "大学", "自然", "语言", "处理", "人工", "智能", "深度", "学习"]
    print(f"\n  {'词语':<8} {'互信息':>8}  {'说明'}")
    print("  " + "-" * 45)

    for word in words:
        mi = compute_mutual_information(word, text)
        note = "高凝聚度" if mi > 2.0 else "中等" if mi > 1.0 else "低凝聚度"
        bar = "#" * max(0, int(mi))
        print(f"  {word:<8} {mi:>8.4f}  {bar} {note}")

    print("""
    观察：
      - "北京" 的互信息很高 → "北" 和 "京" 经常一起出现
      - "深度" 的互信息较高 → "深" 和 "度" 经常一起出现
      - 互信息高的字符串更可能是一个词
    """)


def lesson_entropy():
    """第二部分：左右熵"""

    print_separator("11.2 左右熵（Left/Right Entropy）")

    print("""
    左右熵衡量一个词的"上下文多样性"。

    核心思想：
      一个独立的词，应该能和很多不同的词搭配。
      如果一个字符串只能和特定的词搭配，它可能不是独立的词。

    生活类比：
      想象一个人：
      - 如果他的左边总是同一个人 → 他的"左熵"低（上下文单一）
      - 如果他的左边总是不同的人 → 他的"左熵"高（上下文多样）

      左右熵高 → 这个词是一个独立的词
      左右熵低 → 这个词可能只是一个片段
    """)

    text = """
    我今天去了北京大学，北京大学是中国最好的大学之一。
    北京大学的校园很美，北京大学的图书馆很大。
    我在北京大学学习自然语言处理。
    自然语言处理是人工智能的一个分支。
    人工智能技术发展很快，深度学习是核心技术。
    吃葡萄不吐葡萄皮，不吃葡萄倒吐葡萄皮。
    """

    # 演示 1：左右熵计算
    print("-" * 40)
    print("示例 1：左右熵计算")

    words = ["北京", "大学", "葡萄", "自然", "处理", "的", "了"]
    print(f"\n  {'词语':<8} {'左熵':>8} {'右熵':>8} {'最小熵':>8}  {'说明'}")
    print("  " + "-" * 55)

    for word in words:
        left_ent, right_ent = compute_left_right_entropy(word, text)
        min_ent = min(left_ent, right_ent)
        note = "独立词" if min_ent > 1.0 else "可能不是词" if min_ent < 0.5 else "不确定"
        print(f"  {word:<8} {left_ent:>8.4f} {right_ent:>8.4f} {min_ent:>8.4f}  {note}")

    print("""
    观察：
      - "葡萄" 的左右熵高 → 可以和很多词搭配（吃葡萄、买葡萄...）
      - "的" 的左右熵高 → 可以和很多词搭配
      - 左右熵高表示这个词是"独立的"
    """)


def lesson_new_word_discovery():
    """第三部分：新词发现算法"""

    print_separator("11.3 新词发现算法")

    print("""
    新词发现的核心思路：
      1. 候选生成：提取所有可能的 n-gram
      2. 频率过滤：去掉出现次数太少的
      3. 互信息过滤：去掉内部凝聚度低的
      4. 左右熵过滤：去掉上下文单一的
      5. 综合排序：按得分排序，输出最可能的新词

    生活类比：
      就像在人群中找"新人"：
      1. 先列出所有出现过的人（候选生成）
      2. 过滤掉只出现一两次的（频率过滤）
      3. 过滤掉总是和同一个人一起出现的（互信息过滤）
      4. 过滤掉上下文太单一的（左右熵过滤）
      5. 按"出现次数"和"社交活跃度"排序（综合排序）
    """)

