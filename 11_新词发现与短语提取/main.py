def lesson_phrase_extraction():
    """第四部分：短语提取算法"""

    print_separator("11.4 短语提取算法")

    print("""
    短语提取和新词发现类似，但更关注"多词组合"。

    核心思想：
      一个短语是由多个词组成的，这些词经常一起出现，
      而且不能被其他词替换。

    生活类比：
      想象你在学习成语：
      - "一举两得" 是一个短语（经常一起出现，不能拆开）
      - "一 举 两 得" 不是短语（只是四个独立的字）

    常用方法：
      1. PMI（点互信息）：衡量词与词之间的关联程度
      2. 左右熵：衡量短语的独立性
      3. 综合得分：PMI * 左右熵
    """)

    text = """
    自然语言处理是人工智能的重要方向。
    人工智能技术发展迅速，深度学习是核心技术。
    深度学习在自然语言处理中应用广泛。
    机器学习也是人工智能的重要分支。
    机器学习和深度学习密切相关。
    自然语言处理需要机器学习的支持。
    北京大学的自然语言处理实验室很厉害。
    清华大学也是中国顶尖的大学。
    """

    print("-" * 40)
    print("示例 1：短语提取结果")

    phrases = extract_phrases(text, min_freq=2, top_k=10)

    print(f"\n  {'排名':<4} {'短语':<8} {'得分':>8} {'频率':>6}")
    print("  " + "-" * 30)

    for rank, (phrase, score, count) in enumerate(phrases, 1):
        bar = "#" * max(0, int(score * 3))
        print(f"  {rank:<4} {phrase:<8} {score:>8.4f} {count:>6}  {bar}")

    print("""
    观察：
      - 得分高的短语，内部词之间的关联更紧密
      - 这些短语更可能是一个完整的语义单元
    """)


